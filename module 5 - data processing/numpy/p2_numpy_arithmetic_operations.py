import numpy as np

a = np.array([7,3,4,5,1])
b = np.array([3,4,5,6,7])

# add or +
print(np.add(a, b))
print(a+b)

# subtract or -
print(np.subtract(a, b))
print(a-b)

# mutiply
print(np.multiply(a, b))
print(a*b)

# divide
print(np.divide(a,b))
print(a/b)

# remainder 
print(np.remainder(a,b))

# mod
print(np.mod(a,b))

# power
print(np.power(a,b))
