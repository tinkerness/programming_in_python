import numpy as np 
arr = np.array([1, 2, 3, 4, 5]) 
print(arr) 

# We can also pass a tuple in the array function to create an array.
arr2 = np.array((3, 4, 5)) 
print(arr2) 

# Two Dimensional Arrays
arr3 = np.array([[1, 2, 3], [4, 5, 6]]) 
print(arr3)

# Three Dimensional Arrays
arr4 = np.array([[[1, 2, 3], [4, 5, 6]], [[1, 2, 3], [4, 5, 6]]])
print(arr4) 