import turtle
t=turtle.Turtle()

# Set the orientation of the turtle to to_angle.
# 0 - east , 90 - north , 180 - west , 270 - south
t.setheading(45)
# Move the turtle forward by the specified distance.
t.forward(50)
# Turn turtle left by angle units.
t.left(90)
t.forward(50)

turtle.done()